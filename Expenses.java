    private void Table5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table5MouseClicked
       
      int row = Table5.getSelectedRow();
       select = Table5.getModel().getValueAt(row,2).toString();
       String sql = "SELECT * FROM `expenses` WHERE  'To'="+ select +"";
       try{
           Class.forName("com.mysql.jdbc.Driver");
           conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookshop","root","");
           ps = conn.createStatement();
          rs=ps.executeQuery(sql);
          
          if(rs.next())
           {
               to.setText(rs.getString("To"));
               reason.setText(rs.getString("Reason"));
               amount.setText(rs.getString("Amount"));
           }
       }catch(ClassNotFoundException | SQLException e){
        JOptionPane.showMessageDialog(null,e);
    }
    }//GEN-LAST:event_Table5MouseClicked

