    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql="INSERT INTO `purchase`(`dname`, `bname`, `bqty`, `price`, `tprice`, `tax`, `date`) VALUES (?,?,?,?,?,?,?)";
        try {
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookshop","root","");
            pst=con.prepareStatement(sql);
            pst.setString(1,dname.getText());
            pst.setString(2,bname.getText());
            pst.setString(3,(String)bqty.getSelectedItem());
            pst.setString(4,price.getText());
            pst.setString(5,tprice.getText());
            pst.setString(6,tax.getText());
            pst.setString(7,((JTextField)date.getDateEditor().getUiComponent()).getText());
            pst.execute();
            JOptionPane.showMessageDialog(rootPane,"Record Inserted", "Success",JOptionPane.INFORMATION_MESSAGE); 
            DisplayTable();
            clear();
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

