    private void Table4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table4MouseClicked
        
         int row = Table4.getSelectedRow();
       select = Table4.getModel().getValueAt(row,0).toString();
       String sql = "select * from purchase where dname='"+ select + "'";
       try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookshop","root","");
           ps = con.createStatement();
          rs=ps.executeQuery(sql);
          
          if(rs.next())
           {
               dname.setText(rs.getString("dname"));
               bname.setText(rs.getString("bname"));
               bqty.setSelectedItem(rs.getString("bqty"));
               price.setText(rs.getString("price"));
               tprice.setText(rs.getString("tprice"));
               tax.setText(rs.getString("tax"));
               //date.setSelectedDate(rs.getString("date"));
           }
       }catch(ClassNotFoundException | SQLException e){
        JOptionPane.showMessageDialog(null,e);
    }
    }//GEN-LAST:event_Table4MouseClicked

