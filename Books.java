    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String bookname=bname.getText();
        String authorname=aname.getText();
        String publication=(String)pub.getSelectedItem();
        String btype=(String)type.getSelectedItem();
        String bprice=price.getText();
        String bstock=(String) stock.getSelectedItem();
        
        if(bookname.trim().equals("")&& authorname.trim().equals("") && bprice.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane,"Please Fill All The Fields", "Warning",JOptionPane.WARNING_MESSAGE);
        }else{
            
           
            String sql="INSERT INTO `book`(`bname`, `aname`, `publication`, `booktype`, `price`, `stock`) VALUES (?,?,?,?,?,?)";
            try {
        
             con=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookshop","root","");
             pst=con.prepareStatement(sql);
             pst.setString(1, bname.getText());
             pst.setString(2, aname.getText());
             pst.setString(3, (String)pub.getSelectedItem());
             pst.setString(4, (String)type.getSelectedItem());
             pst.setString(5, price.getText());
             pst.setString(6, (String)stock.getSelectedItem());
             pst.executeUpdate();
             JOptionPane.showMessageDialog(rootPane,"Record Inserted", "Success",JOptionPane.INFORMATION_MESSAGE); 
             clear();
            }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
            } 
            try {
                DisplayTable();
            } catch (SQLException ex) {
                Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
       
        
        
                
    }//GEN-LAST:event_jButton2ActionPerformed

