    private void Table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table1MouseClicked
    
      int row=Table1.getSelectedRow();
      select=Table1.getModel().getValueAt(row,0).toString();
      String sql="select * from book where bname= '" +  select + "'";
      try{
          
          pst=con.prepareStatement(sql);
          rs=pst.executeQuery();
          
          if(rs.next())
          {
              bname.setText(rs.getString("bname"));
               aname.setText(rs.getString("aname"));
                pub.setSelectedItem(rs.getString("publication"));
                 type.setSelectedItem(rs.getString("booktype"));
                  price.setText(rs.getString("price"));
                   stock.setSelectedItem(rs.getString("stock"));
              
          }
      }catch(SQLException e){
      JOptionPane.showMessageDialog(null, e);
      }
        
        
        
    }//GEN-LAST:event_Table1MouseClicked

